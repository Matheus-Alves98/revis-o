a = int(input("Digite o primeiro número: "))
b = int(input("Digite o segundo número: "))
c = int(input("Digite o terceiro número: "))
soma = a+b
if soma < c:
    print(f"a soma entre A e B = {soma} é menor que C = {c} !")
else:
    print(f"a soma entre A e B = {soma} não é menor que C = {c} !")